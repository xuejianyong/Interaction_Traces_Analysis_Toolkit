package dl;

import java.util.ArrayList;
import java.util.List;

public class Action {

	public int actionType;
	public Interaction enactedAction;
	
	public int totalValence = 0;//之前所有的action取得的valence
	public int num = 0;
	public double meanValence = 0.00;//之前所有的采用这个action的平均valence
	
	public List<Interaction> enactedList = new ArrayList<Interaction>();
	
	public void increaseNum() {
		this.num++;
	}
	
	
	public int getActionType() {
		return actionType;
	}
	public void setActionType(int actionType) {
		this.actionType = actionType;
	}
	public Interaction getEnactedAction() {
		return enactedAction;
	}
	public void setEnactedAction(Interaction enactedAction) {
		this.enactedAction = enactedAction;
	}
	public int getTotalValence() {
		return totalValence;
	}
	public void setTotalValence(int totalValence) {
		this.totalValence = totalValence;
	}
	public double getMeanValence() {
		return meanValence;
	}
	public void setMeanValence(double meanValence) {
		this.meanValence = meanValence;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public List<Interaction> getEnactedList() {
		return enactedList;
	}
	public void setEnactedList(List<Interaction> enactedList) {
		this.enactedList = enactedList;
	}
	
	
	
}
